func enhanceEnv(t tokens) string {
	return fmt.Sprintf("ENHANCE_THEME=%s\n", t.Tools.GhEnhanceTheme)
}

func diffnavTheme(t tokens) string {
	return fmt.Sprintf(`ui:
  sideBySide: true
  icons: %s
  colorFileNames: true
  showDiffStats: true
  hideHeader: false
`, t.Font.Icons)
}

func deltaConfig(t tokens) string {
	return fmt.Sprintf(`[delta]
    syntax-theme = %s
    side-by-side = true
    line-numbers = true
    navigate = true
    minus-style                = syntax %q
    minus-emph-style           = syntax %q
    plus-style                 = syntax %q
    plus-emph-style            = syntax %q
    file-style                 = %q bold
    hunk-header-style          = %q
    line-numbers-minus-style   = %q
    line-numbers-plus-style    = %q
`, t.Diff.SyntaxTheme, t.Diff.RemoveBG, t.Diff.RemoveEmphasisBG, t.Diff.AddBG, t.Diff.AddEmphasisBG, t.Accent, t.Text.Secondary, t.Diff.Remove, t.Diff.Add)
}

func glamourTheme(t tokens) string {
	bold := true
	style := map[string]any{
		"document":    map[string]any{"color": t.Text.Primary},
		"paragraph":   map[string]any{"color": t.Text.Primary},
		"heading":     map[string]any{"color": t.Accent, "bold": bold},
		"h1":          map[string]any{"color": t.Accent, "bold": bold},
		"h2":          map[string]any{"color": t.Accent, "bold": bold},
		"h3":          map[string]any{"color": t.Accent2, "bold": bold},
		"text":        map[string]any{"color": t.Text.Primary},
		"strong":      map[string]any{"color": t.Text.Primary, "bold": bold},
		"emph":        map[string]any{"color": t.Text.Secondary},
		"hr":          map[string]any{"color": t.Border.Primary},
		"link":        map[string]any{"color": t.Accent2, "underline": true},
		"link_text":   map[string]any{"color": t.Accent2},
		"code":        map[string]any{"color": t.Text.Primary, "background_color": t.Border.Faint},
		"code_block":  map[string]any{"color": t.Text.Primary, "theme": t.Diff.SyntaxTheme},
		"item":        map[string]any{"color": t.Text.Primary},
		"enumeration": map[string]any{"color": t.Text.Secondary},
	}
	data, err := json.MarshalIndent(style, "", "  ")
	must(err)
	return string(data) + "\n"
}
